#include <unistd.h>

void	ft_add(int a, int *ptr)
{
	*ptr = *ptr + a;
}
int main ()
{
	int a ;

	a = 5;
	int b = 5;
	int *ptr = &b;
	ft_add(a, ptr);
	write(1, &ptr, 1);
	return 0;
}
